#!/usr/bin/env node
/**
 * Scans apps/web/src/app/api route files and generates handler route tables
 * for packages/api-handlers/src/generated/route-manifest.ts
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const apiRoot = path.join(root, 'apps/web/src/app/api');
const outFile = path.join(root, 'packages/api-handlers/src/generated/route-manifest.ts');

function walk(dir, base = '') {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const routes = [];
  for (const entry of entries) {
    const rel = base ? `${base}/${entry.name}` : entry.name;
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      routes.push(...walk(full, rel));
      continue;
    }
    if (entry.name !== 'route.ts') continue;

    const content = fs.readFileSync(full, 'utf8');
    const methods = [];
    for (const m of ['GET', 'POST', 'PUT', 'PATCH', 'DELETE']) {
      if (new RegExp(`export\\s+async\\s+function\\s+${m}\\b`).test(content)) {
        methods.push(m);
      }
    }
    if (!methods.length) continue;

    const routePath = rel.replace(/\/route\.ts$/, '').replace(/\[(\.\.\.)?([^\]]+)\]/g, (_, dots, name) => {
      return dots ? `:${name}*` : `:${name}`;
    });

    routes.push({
      file: full.replace(/\\/g, '/'),
      rel: routePath,
      methods,
      importPath: full
        .replace(/\\/g, '/')
        .replace(/.*\/apps\/web\/src\/app\/api\//, '')
        .replace(/\/route\.ts$/, ''),
    });
  }
  return routes;
}

function domainOf(rel) {
  return rel.split('/')[0] || 'root';
}

const all = walk(apiRoot);
const byDomain = {};
for (const r of all) {
  const d = domainOf(r.importPath);
  if (!byDomain[d]) byDomain[d] = [];
  byDomain[d].push(r);
}

fs.mkdirSync(path.dirname(outFile), { recursive: true });

const lines = [
  '// AUTO-GENERATED by scripts/generate-api-handlers.mjs — do not edit',
  '',
  'export type GeneratedRoute = {',
  '  method: string;',
  '  pattern: string;',
  '  source: string;',
  '};',
  '',
];

for (const [domain, routes] of Object.entries(byDomain).sort()) {
  lines.push(`export const ${domain.replace(/[^a-zA-Z0-9]/g, '_')}Routes: GeneratedRoute[] = [`);
  for (const r of routes.sort((a, b) => a.rel.localeCompare(b.rel))) {
    for (const method of r.methods) {
      const pattern = '/' + r.rel.split('/').slice(1).join('/');
      lines.push(`  { method: '${method}', pattern: '${pattern || '/'}', source: '${r.importPath}' },`);
    }
  }
  lines.push('];');
  lines.push('');
}

lines.push('export const routeManifest = {');
for (const domain of Object.keys(byDomain).sort()) {
  const key = domain.replace(/[^a-zA-Z0-9]/g, '_');
  lines.push(`  ${JSON.stringify(domain)}: ${key}Routes,`);
}
lines.push('} as const;');
lines.push('');

fs.writeFileSync(outFile, lines.join('\n'));
console.log(`Wrote ${all.length} route files (${Object.keys(byDomain).length} domains) to ${outFile}`);
