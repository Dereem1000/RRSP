import re
import shutil
import sys
from pathlib import Path


def find_npm() -> str | None:
    for name in ("npm.cmd", "npm.exe", "npm"):
        found = shutil.which(name)
        if found:
            return found
    return None


def npm_command(*args: str) -> list[str]:
    """
    Build argv to run npm on Windows (avoids WinError 2 when npm is not directly executable).
    """
    if sys.platform == "win32":
        return ["cmd", "/c", "npm", *args]
    npm = find_npm() or "npm"
    return [npm, *args]


