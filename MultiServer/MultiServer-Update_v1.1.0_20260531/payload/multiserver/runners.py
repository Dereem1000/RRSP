def write_flask_launcher(project_dir: Path, port: int, script_name: str = "") -> Path:
    """
    Flask apps on Windows (emoji logs, cp1252).
    Uses `flask run --port` so apps with hardcoded app.run(port=5000) still bind correctly.
    When script_name is set (e.g. /demo/repair-restaurant), writes a WSGI entry so
    url_for() links work behind the public /demo/<slug>/ path.
    """
    project_dir = project_dir.resolve()
    ms_dir = project_dir / ".multiserver"
    ms_dir.mkdir(exist_ok=True)
    launch = ms_dir / "launch.bat"
    entry = ms_dir / "entry.py"
    prefix = (script_name or "").strip().rstrip("/")
    entry.write_text(
        "# Generated by MultiServer — do not edit.\n"
        "import os\n"
        "import sys\n"
        "from pathlib import Path\n"
        "\n"
        "_root = Path(__file__).resolve().parent.parent\n"
        "os.chdir(_root)\n"
        "if str(_root) not in sys.path:\n"
        "    sys.path.insert(0, str(_root))\n"
        "\n"
        "from app import app  # noqa: E402\n"
        "\n"
        "_prefix = os.environ.get('MULTISERVER_SCRIPT_NAME', '').strip().rstrip('/')\n"
        "if _prefix:\n"
        "    class _ScriptNameMiddleware:\n"
        "        def __init__(self, wsgi_app, script_name):\n"
        "            self.wsgi_app = wsgi_app\n"
        "            self.script_name = script_name\n"
        "\n"
        "        def __call__(self, environ, start_response):\n"
        "            environ['SCRIPT_NAME'] = self.script_name\n"
        "            return self.wsgi_app(environ, start_response)\n"
        "\n"
        "    app.wsgi_app = _ScriptNameMiddleware(app.wsgi_app, _prefix)\n"
        "\n"
        "if __name__ == '__main__':\n"
        "    _port = int(os.environ.get('PORT', '5000'))\n"
        "    app.run(host='0.0.0.0', port=_port, debug=False, use_reloader=False)\n",
        encoding="utf-8",
    )
    script_env = f"set MULTISERVER_SCRIPT_NAME={prefix}\r\n" if prefix else ""
    launch.write_text(
        f"@echo off\r\n"
        f"chcp 65001 >nul\r\n"
        f'cd /d "{project_dir}"\r\n'
        f"set PYTHONUTF8=1\r\n"
        f"set PYTHONIOENCODING=utf-8\r\n"
        f"set PORT={port}\r\n"
        f"{script_env}"
        f"if not exist .env (\r\n"
        f"  if exist setup_secrets.py (\r\n"
        f"    echo [MultiServer] No .env found — running setup_secrets.py...\r\n"
        f"    call python setup_secrets.py\r\n"
        f"    if errorlevel 1 exit /b 1\r\n"
        f"  )\r\n"
        f")\r\n"
        f"set FLASK_APP=.multiserver\\entry\r\n"
        f"set DEV_MODE=1\r\n"
        f"set FLASK_ENV=development\r\n"
        f"set BROWSER=none\r\n"
        f"call python -X utf8 -m flask run --host 0.0.0.0 --port {port} --no-reload\r\n"
        f"if errorlevel 1 (\r\n"
        f"  echo Flask CLI failed, trying entry.py fallback>>\"%TEMP%\\multiserver-flask.log\"\r\n"
        f"  call python -X utf8 .multiserver\\entry.py\r\n"
        f")\r\n",
        encoding="utf-8",
    )
    return launch


